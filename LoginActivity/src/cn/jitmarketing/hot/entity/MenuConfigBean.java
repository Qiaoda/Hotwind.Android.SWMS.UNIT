package cn.jitmarketing.hot.entity;

public class MenuConfigBean {

	private Class<?> activityClass;
	private Integer bgDrawableID;
	private Integer iconDrawableID;
	
	public Class<?> getActivityClass() {
		return activityClass;
	}
	public void setActivityClass(Class<?> activityClass) {
		this.activityClass = activityClass;
	}
	public Integer getBgDrawableID() {
		return bgDrawableID;
	}
	public void setBgDrawableID(Integer bgDrawableID) {
		this.bgDrawableID = bgDrawableID;
	}
	public Integer getIconDrawableID() {
		return iconDrawableID;
	}
	public void setIconDrawableID(Integer iconDrawableID) {
		this.iconDrawableID = iconDrawableID;
	}
	
	
}
